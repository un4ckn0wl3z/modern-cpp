// Assignment Operators Example.
#include <iostream>
#include <string>

int main()
{
    int myInt = 5;

    myInt += 5;
    std::cout << myInt << std::endl;

    myInt -= 5;
    std::cout << myInt << std::endl;

    myInt *= 5;
    std::cout << myInt << std::endl;

    myInt /= 5;
    std::cout << myInt << std::endl;

    myInt %= 5;
    std::cout << myInt << std::endl;
}